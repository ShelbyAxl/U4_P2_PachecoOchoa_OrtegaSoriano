    public boolean ready(){
        return cmbNombre.getSelectedIndex() != 0 && !txtModelo.getText().equals("") &&
        !txtMarca.getText().equals("") && !txtPrecio.getText().equals("") && 
        !txtExistencia.getText().equals("");
    }
    
