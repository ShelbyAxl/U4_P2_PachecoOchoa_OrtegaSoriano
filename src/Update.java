    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        
        if(ready()){
            Document datos = new Document();

            datos.put("Nombre", cmbNombre.getSelectedItem());
            datos.put("Modelo", txtModelo.getText());
            datos.put("Marca", txtMarca.getText());
            datos.put("Precio", Float.parseFloat(txtPrecio.getText()));
            datos.put("Existencia", Integer.parseInt(txtExistencia.getText()));

            UpdateResult res = componente.updateOne(filtro, new Document("$set", datos));

            if(res.getMatchedCount() == 1)showMessageDialog(this,"Actualizado satisfactoriamente");
            else showMessageDialog(this,"Hubo un error!");
        }
        else showMessageDialog(this,"Error! introduzca todos los datos solicitados");
    }//GEN-LAST:event_btnUpdateActionPerformed

